function x = nmtridsolve(a,b,c,d)
n = length(b);
y(1)=c(1)/b(1);
p(1)=d(1)/b(1);
for i = 2:n
    if i < n
        y(i)=c(i)/(b(i)-a(i-1)*y(i-1));
    end    
    p(i)=(d(i)-a(i-1)*p(i-1))/(b(i)-a(i-1)*y(i-1));
end
%% 回代
x(n,1)= p(n);
for i = n-1:-1:1
    x(i,1) = p(i)-y(i)*x(i+1);
end
%% (b)另附文档
%Thomas算法在矩阵是对角优势矩阵（无论在行还是列来看）或矩阵是正定矩阵时，该算法存在数值的稳定性。反之，该算法就是数值不稳定的，即若ai的范数与ci的范数之和大于bi的范数时，算法是数值不稳定的，因为在迭代的过程中可能会出现主元接近0的状况，从而引发较大的数值误差。
